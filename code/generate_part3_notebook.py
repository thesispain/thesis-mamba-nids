        "\n",
        "# --- Custom BERT Packet Embedder (Matching bert_cutmix_v5_partial.pth) ---\n",
        "class PacketEmbedderBert(nn.Module):\n",
        "    def __init__(self, d_model=256):\n",
        "        super().__init__()\n",
        "        self.proto_emb = nn.Embedding(256, 32)\n",
        "        self.flags_emb = nn.Embedding(64, 32)\n",
        "        self.dir_emb   = nn.Embedding(2, 8)\n",
        "        self.loglen_proj = nn.Linear(1, 32)\n",
        "        self.iat_proj  = nn.Linear(1, 32)\n",
