        "        proto  = x[:,:,0].long().clamp(0, 255)\n",
        "        length = x[:,:,1:2]\n",
        "        flags  = x[:,:,2].long().clamp(0, 63)\n",
        "        iat    = x[:,:,3:4]\n",
